function B_m=strain_operator_m(x,r,s)

J=jacobian(x,r,s);

B_m(1:2,:)=1/4*inv(J)*[(1+s), -(1+s), -(1-s), (1-s), 0, 0, 0, 0;
                  0, 0, 0, 0, (1+r), (1-r), -(1-r), -(1+r)];

B_m(3,:)=sum(1/4*inv(J)*[(1+r), (1-r), -(1-r), -(1+r), 0, 0, 0, 0;
                    0, 0, 0, 0, (1+s), -(1+s), -(1-s), (1-s)],1);

end