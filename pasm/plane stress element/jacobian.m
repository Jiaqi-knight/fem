function J=jacobian(x,r,s)

J(1,1)=x(1,1)*1/4*((1+s)+x(2,1)*(-(1+s))+...
    x(3,1)*(-(1-s))+x(4,1)*(1-s));

J(1,2)=x(1,2)*1/4*((1+s)+x(2,2)*(-(1+s))+...
    x(3,2)*(-(1-s))+x(4,2)*(1-s));

J(2,1)=x(1,1)*1/4*((1+r)+x(2,1)*(1-r)+...
    x(3,1)*(-(1-r))+x(4,1)*(-(1+r)));

J(2,2)=x(1,2)*1/4*((1+r)+x(2,2)*(1-r)+...
    x(3,2)*(-(1-r))+x(4,2)*(-(1+r)));

end